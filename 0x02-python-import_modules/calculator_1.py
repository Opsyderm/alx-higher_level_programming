#!/usr/bin/python3
def add(a, b):
    """My sum function

    Args:
        a: first integer
        b: second integer

    Returns:
        The return value. a + b
    """
    return (a + b)


def sub(a, b):
    """My difference function

    Args:
        a: first integer
        b: second integer

    Returns:
        The return value. a - b
    """
    return (a - b)


def mul(a, b):
    """the multiplication function

    Args:
        a: first integer
        b: second integer

    Returns:
        The return value. a * b
    """
    return (a * b)


def div(a, b):
    """the division function

    Args:
        a: first integer
        b: second integer

    Returns:
        return value. a / b
    """
    return int(a / b)
